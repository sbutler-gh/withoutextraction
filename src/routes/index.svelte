<h3 style="text-align: center;">There are ways to earn <span>better income</span>,<br>
<span>in reciprocation with nature.</span></h3>

<div class="alt-list" style="text-align: center; display: flex; flex-wrap: wrap; justify-content: space-between">
    <div style="max-width: 300px;">
        <h4>Local filmmaking / Media</h4>
        <p>The better a place is cared for, the more people will want to see it.</p>
        <p><a href="https://filmmakermagazine.com/113084-alex-pritz-the-territory-sundance-2022/">The Territory</a> and <a href="https://ifnotusthenwho.me/">INUTW</a> show that communities are producing film on the ground.</p>
        <p>Digital content is generating billions every year.  Frontline and Global South communities are capable of producing the media set in their own places, and benefitting from legacies of their care themselves.</p>
    </div>
    <div style="max-width: 300px;">
        <h4>Local science / research</h4>
        <p>The better a place is cared for, the more cures, discoveries, and innovations it holds.</p>
        <p>Pharmaceuticals have a history of <a href="https://en.wikipedia.org/wiki/Bioprospecting#Biopiracy">biopiracy</a> medicinal research and innovations in places of rich biodiversity â€” without sharing incomes with the communities themselves.</p>
